// Напишіть програму, яка обчислить суму всіх чисел (за допомогою циклу for) від 1 до 100, які діляться на 3 або на 5.
// І вивести в консоль рядок `Сума чисел, які діляться на 3 або на 5 від 1 до 100 = ${sum}`

let sum = 0;

for (let i = 1; i <= 100; i++) {
    if (i % 3 === 0 || i % 5 === 0) {
        sum += i;
    }
}

console.log(`Сума чисел, які діляться на 3 або на 5 від 1 до 100 = ${sum}`);